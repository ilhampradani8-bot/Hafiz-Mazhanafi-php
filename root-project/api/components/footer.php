    const line3 = document.getElementById('line3');
    let isMenuOpen = false;

    function toggleMenu() { if (!isMenuOpen) openMenu(); else closeMenu(); }
    function openMenu() {
        if(!navMenu) return;
        isMenuOpen = true;
        navMenu.classList.remove('-translate-y-[150%]');
        overlay.classList.remove('hidden');
        line1.classList.add('rotate-45', 'translate-y-[8px]');
        line2.classList.add('opacity-0');
        line3.classList.add('-rotate-45', '-translate-y-[8px]');
        document.body.style.overflow = 'hidden';
    }
    function closeMenu() {
        if(!navMenu) return;
        isMenuOpen = false;
        navMenu.classList.add('-translate-y-[150%]');
        overlay.classList.add('hidden');
        line1.classList.remove('rotate-45', 'translate-y-[8px]');
        line2.classList.remove('opacity-0');
        line3.classList.remove('-rotate-45', '-translate-y-[8px]');
        document.body.style.overflow = 'auto';
    }
    document.querySelectorAll('.mobile-link').forEach(link => { link.addEventListener('click', closeMenu); });

    // Scroll Logic
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if(!navbar) return;
        if (window.scrollY > 50) {
            navbar.classList.add('bg-black/90', 'shadow-lg');
            navbar.classList.remove('bg-black/20');
        } else {
            navbar.classList.remove('bg-black/90', 'shadow-lg');
            navbar.classList.add('bg-black/20');
        }
    });

